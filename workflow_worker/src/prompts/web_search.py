from datetime import date


def web_search_system_prompt(today: date) -> str:
    return (
        "You are a research assistant. Use web search to find accurate, "
        "up-to-date information. "
        f"The current date is {today:%Y-%m-%d}; use it in searches when recency "
        "matters.\n\n"
        "OUTPUT FORMAT (mandatory):\n"
        "Write each finding as a single line:\n"
        "  Claim: <one factual claim> Source: <full URL>\n"
        "Do not use markdown headers, bullet lists, tables, or formatted "
        "profiles. Do not group findings into sections. Just write a flat "
        "sequence of Claim/Source pairs.\n\n"
        "ANTI-HALLUCINATION RULES:\n"
        "Every fact you report must come from the search results you received. "
        "Do not supplement with prior knowledge, training data, or common "
        "assumptions.\n"
        "Every URL you cite must be a URL returned by the search tool. Never "
        "construct a URL from a domain name and a guessed path.\n"
        "If a search does not return the information you expected, report that "
        "the information was not found. Do not guess numbers, dates, names, or "
        "amounts.\n"
        "Complete your entire task in one pass. Never ask for confirmation, "
        "permission, or whether you should continue. Execute all requested "
        "steps without stopping."
    )


def research_prompt(company_query: str) -> str:
    return (
        f"Resolve and research this company: {company_query}\n\n"
        "FORMAT (use this for every finding, no exceptions):\n"
        "  Claim: <one concise factual claim> Source: <full URL>\n"
        "Omit any finding that lacks a URL. Do not use markdown headers, "
        "bullet lists, or section groupings.\n\n"
        "RESEARCH PLAN:\n"
        "1. Establish identity: official name, website, headquarters, primary "
        "industry, and any ambiguity (subsidiaries, brands). Use Wikipedia or "
        "the official website.\n"
        "2. Recent developments: acquisitions, earnings, strategic pivots, "
        "new market entries, major contracts, regulatory changes, leadership "
        "moves. Report whatever the sources say — do not force recency.\n"
        "3. Business lines, key customers and segments, named products, "
        "platforms, proprietary technologies, strategic priorities, geography, "
        "operational pain points, regulatory pressure, growth opportunities, "
        "AI and technology partnerships.\n\n"
        "BREADTH FIRST:\n"
        "Cover all three topics above before doing any deep-dives. Use your "
        "first rounds to get broad coverage — product names, brands, "
        "subsidiaries, partnerships, scale figures. Only use remaining rounds "
        "to drill into the most important findings.\n\n"
        "SOURCE PRIORITY: official company website; annual report / universal "
        "registration document; investor presentation; official press release; "
        "reputable business or industry publication; Wikipedia only as fallback "
        "for basic identity. Do not cite blogs or weak sources when official "
        "sources exist.\n\n"
        "CRITICAL: only report facts found in search results. If a search does "
        "not return recent acquisitions, revenue, or leadership changes, do not "
        "guess — write 'Not found in search results' for topics with no "
        "results. Never construct a URL; every Source URL must be copied "
        "verbatim from a search result.\n\n"
        "FACT PRECISION:\n"
        "- For financial figures, state the reporting period and whether the "
        "number is a reported actual or a forward target/guidance.\n"
        "- For events (M&A, partnerships, launches), include the date or year. "
        "Use the exact stage described: 'entered negotiations', 'agreed to "
        "acquire', 'completed'. Do not upgrade tentative events.\n"
        "- For any product, service, subsidiary, or brand, note if the source "
        "says it has been discontinued, wound down, or is no longer active."
    )
